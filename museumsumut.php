        <h1 id="destination-title">MUSEUM NEGERI SUMATERA UTARA</h1>
        <p>Di tengah geliat Kota Medan yang modern, terdapat sebuah gerbang waktu yang siap membawa Anda menelusuri ribuan tahun jejak peradaban di tanah Sumatera Utara. Tempat itu adalah Museum Negeri Provinsi Sumatera Utara, sebuah institusi yang tak hanya menyimpan benda, tetapi juga menjaga jiwa dan cerita dari berbagai zaman. Dikenal luas oleh masyarakat lokal dengan sebutan "Gedung Arca", museum ini adalah destinasi wajib bagi siapa saja yang ingin memahami kekayaan sejarah dan budaya provinsi ini secara mendalam.</p>

        <p>Sebagai museum terbesar di Sumatera Utara, tempat ini bukanlah sekadar ruang pamer yang sunyi. Ia adalah sebuah narasi besar yang hidup, dimulai dari era prasejarah, masa kerajaan Hindu-Buddha, kesultanan Islam, perjuangan kemerdekaan, hingga keberagaman etnis yang membentuk wajah Sumatera Utara hari ini. Dengan koleksi yang sangat kaya dan penataan yang informatif, kunjungan ke sini adalah sebuah petualangan edukatif yang mencerahkan dan sangat terjangkau.</p>

        <img src="image/unnamed.jpg">
        <h2>Harta Karun Peradaban: Menelusuri Koleksi Museum</h2>
        
        <p>Dengan arsitektur bangunan yang mengadopsi ornamen tradisional khas Sumatera Utara, museum ini sendiri sudah menjadi sebuah pemandangan yang menarik. Memasuki dalamnya, Anda akan diajak dalam sebuah perjalanan yang terbagi dalam dua lantai.</p>

        <p>Lantai Pertama: Jejak Masa Lampau
        Petualangan Anda dimulai dari sini, menelusuri zaman prasejarah Sumatera Utara melalui koleksi kapak genggam, peninggalan zaman batu, hingga tembikar kuno. Perjalanan berlanjut ke masa keemasan kerajaan Hindu-Buddha, di mana Anda bisa melihat koleksi arca-arca megah yang menjadi asal-usul julukan "Gedung Arca". Salah satu primadonanya adalah patung Makara (hewan mitologi) dari situs Percandian Padang Lawas. Lantai ini juga memamerkan peninggalan dari masa kesultanan Islam dan era kolonialisme, memberikan gambaran utuh tentang lapisan-lapisan sejarah yang membentuk daerah ini.</p>
       
        <p>Lantai Kedua: Perjuangan dan Keberagaman Etnis
        Naik ke lantai dua, Anda akan disambut oleh diorama dan koleksi yang menceritakan semangat perjuangan para pahlawan Sumatera Utara dalam merebut kemerdekaan. Namun, bagian paling memukau di lantai ini adalah galeri etnografi. Di sinilah Anda akan benar-benar menyelami kekayaan budaya provinsi ini. Anda bisa melihat :</p>
        <p>Koleksi Ulos: Ratusan kain tenun tradisional Batak dengan berbagai motif dan makna filosofisnya.</p>
        <p>Busana Adat: Pakaian tradisional dari berbagai etnis seperti Melayu, Karo, Simalungun, Nias, Pakpak, dan lainnya.</p>
        <p>Miniatur Rumah Adat: Replika detail dari rumah-rumah tradisional yang unik.</p>
        <p>Peralatan Keseharian: Berbagai koleksi senjata tradisional, alat musik, peralatan pertanian, dan perlengkapan upacara adat.</p>

       
