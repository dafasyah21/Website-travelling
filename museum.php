        <h1 id="destination-title">RAHMAT INTERNATIONAL WILDLIFE MUSEUM AND GALLERY</h1>
        <p>Bagi Anda yang memiliki jiwa petualang dan rasa ingin tahu yang besar terhadap keanekaragaman hayati dunia, tak perlu jauh-jauh menjelajahi sabana Afrika atau hutan Amazon. Cukup langkahkan kaki Anda ke jantung Kota Medan, Sumatera Utara, di mana sebuah institusi berkelas dunia menanti untuk membawa Anda dalam sebuah perjalanan tak terlupakan. Inilah Rahmat International Wildlife Museum and Gallery, sebuah surga bagi para pecinta alam dan destinasi wisata edukasi yang memukau untuk segala usia.</p>
        <p>Museum ini bukan sekadar ruang pameran biasa. Didirikan oleh Dr. H. Rahmat Shah, seorang konservasionis dan pemburu profesional yang memiliki kecintaan mendalam terhadap alam liar, galeri ini merupakan wujud nyata dari visinya untuk mendekatkan masyarakat dengan kehidupan satwa liar sekaligus menyuarakan pentingnya konservasi. Dengan koleksi yang luar biasa dan penataan diorama yang seolah hidup, museum ini berhasil menjadi salah-tunya galeri satwa liar terlengkap di Asia.</p>

        <img src="image/museum2.jpg">
        <h2>Koleksi Spektakuler dari Seluruh Penjuru Dunia</h2>
        
        <p>Memasuki Rahmat International Wildlife Museum and Gallery, pengunjung akan disambut oleh koleksi lebih dari 2.600 spesies dan 5.600 spesimen yang diawetkan dengan sangat baik (taksidermi). Seluruh koleksi ini diperoleh secara legal dari berbagai belahan dunia, baik melalui perburuan konservasi yang diatur, hasil pemberian dari negara sahabat, maupun satwa yang mati secara alami di kebun binatang.</p>

        <p>Setiap sudut museum dirancang dengan sangat detail, menampilkan satwa-satwa dalam diorama yang menyerupai habitat aslinya. Pengunjung dapat merasakan sensasi berada di tengah-tengah kawanan singa di padang rumput Afrika, berhadapan dengan beruang kutub di lanskap Arktik yang dingin, hingga mengagumi keindahan berbagai jenis burung cendrawasih dari Papua.</p>
        <h2>Beberapa koleksi unggulan yang menjadi daya tarik utama antara lain:</h2>
        <p>The African Big Five: Pameran megah yang menampilkan lima mamalia terbesar dan paling ikonik dari Afrika, yaitu Gajah, Badak Putih, Singa, Banteng, dan Macan Tutul.</br>

        Mountain of Goats: Diorama yang menakjubkan ini memamerkan berbagai spesies kambing gunung dari seluruh dunia dalam sebuah seting pegunungan yang curam.</br>

        Bears of the World: Ruangan khusus yang didedikasikan untuk berbagai jenis beruang dari Amerika, Eropa, dan Asia.</br>

        Indonesian Species: Sebagai wujud kebanggaan akan kekayaan fauna lokal, museum ini juga memiliki koleksi lengkap satwa-satwa endemik Indonesia yang langka dan dilindungi.</p>
        
       
