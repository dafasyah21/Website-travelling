        <h1 id="destination-title">Berastagi</h1>
        <p>Penat dengan hiruk pikuk dan panasnya cuaca kota? Bayangkan Anda membuka jendela di pagi hari dan disambut oleh udara sejuk yang menusuk tulang, hamparan kebun sayur yang hijau membentang sejauh mata memandang, dan siluet gunung yang gagah berdiri di kejauhan. Ini bukan mimpi, ini adalah Berastagi, surga sejuknya Sumatera Utara.
        Terletak di dataran tinggi Karo, sekitar 2 jam perjalanan dari Medan, Berastagi adalah destinasi sempurna untuk melarikan diri sejenak. Lebih dari sekadar Puncak-nya Sumatera Utara, kota ini menawarkan perpaduan sempurna antara keindahan alam, kekayaan budaya, dan keseruan yang tak ada habisnya. Siap untuk petualangan? Mari kita jelajahi pesona Berastagi bersama!</p>

