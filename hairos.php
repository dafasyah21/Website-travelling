        <h1 id="destination-title">HAIROS WATERPARK</h1>
        <p>Saat terik matahari menyapa Kota Medan dan sekitarnya, tidak ada yang lebih menyegarkan daripada menceburkan diri ke dalam kolam yang sejuk. Bagi para pencari kesegaran dan hiburan keluarga, Hairos Waterpark adalah jawaban yang sempurna. Berdiri megah di lahan seluas 17 hektare di kawasan Pancur Batu, Hairos bukan sekadar kolam renang biasa, melainkan sebuah destinasi wisata air terpadu yang menawarkan petualangan seru untuk segala usia.</p>
        <p>Terletak di jalur strategis Jalan Jamin Ginting, Hairos Waterpark telah lama menjadi primadona wisata bagi warga Medan dan Deli Serdang. Dengan konsep one-stop vacation, tempat ini tidak hanya menyajikan beragam wahana air yang menantang, tetapi juga fasilitas pelengkap seperti kebun binatang mini dan area outbound, memastikan setiap anggota keluarga mendapatkan pengalaman liburan yang tak terlupakan.</p>

        <img src="image/hairos.jpg">
        <h2>Surga Wahana Air untuk Semua Kalangan</h2>
        
        <p>Memasuki Hairos Waterpark, Anda akan disambut oleh lanskap luas yang dipenuhi dengan kolam biru jernih dan seluncuran warna-warni yang menjulang. Atmosfer ceria langsung terasa, mengajak Anda untuk segera berganti pakaian dan memulai petualangan air. Aktivitas Populer & Wahana Wajib Coba:</p>

        <p>Bersantai di Kolam Arus (Lazy River): Ingin pengalaman yang lebih rileks? Ambil sebuah ban pelampung dan biarkan diri Anda mengalir santai mengikuti arus lembut di kolam sepanjang 400 meter. Sambil mengapung, nikmati pemandangan taman hijau dan patung-patung unik di sepanjang jalur kolam.</p>
       
        <p>Adrenalin di Menara Seluncur: Bagi para pemberani, menara seluncuran setinggi lebih dari 15 meter menanti untuk ditaklukkan. Pilih antara seluncuran lurus berkecepatan tinggi atau seluncuran spiral yang akan memutar Anda sebelum mendarat dengan seru di kolam.</p>

        <p>Keceriaan untuk Si Kecil: Hairos menyediakan area khusus anak-anak yang aman dan menyenangkan. Terdapat kolam dangkal, seluncuran mini berbentuk gurita, dan wahana ember tumpah raksasa yang selalu menjadi momen yang paling ditunggu-tunggu oleh anak-anak.</p>
       
