        <h1 id="destination-title">MASJID RAYA AL-MASHUN</h1>
        <p> Sebuah monumen megah yang tak hanya menjadi pusat ibadah, tetapi juga saksi bisu kejayaan sejarah dan simbol kebanggaan masyarakat Sumatera Utara. Inilah Masjid Raya Al-Mashun, sebuah mahakarya arsitektur yang memancarkan pesona spiritual dan keindahan yang tak lekang oleh waktu. Mengunjunginya bukan sekadar wisata, melainkan sebuah perjalanan untuk mengagumi perpaduan budaya dan kekayaan histori Kesultanan Deli.</p>
        <p>Dibangun pada tahun 1906 dan rampung pada 1909, masjid ini adalah warisan dari Sultan Ma'mun Al-Rasyid Perkasa Alamsyah dari Kesultanan Deli. Dengan prinsip agung bahwa kemegahan masjid harus melebihi istananya sendiri (Istana Maimun), Sultan mengerahkan sumber daya terbaik untuk menciptakan sebuah tempat ibadah yang spektakuler. Hasilnya adalah sebuah bangunan ikonik yang hingga kini menjadi salah satu masjid terindah di Indonesia.</p>

        <img src="image/raya.jpg">
        <h2>Pesona Arsitektur Tiga Benua</h2>
        
        <p>Daya tarik utama Masjid Raya Al-Mashun terletak pada arsitekturnya yang eklektik dan memukau. Dirancang oleh arsitek Belanda, J.A. Tingdeman, desain masjid ini secara harmonis memadukan tiga gaya arsitektur yang berbeda:</p>

        <p>Timur Tengah (Moorish): Terlihat dari bentuk kubah utama yang menyerupai gaya masjid-masjid di Turki dan Persia. Lengkungan-lengkungan megah di pintu dan jendela juga kental dengan nuansa Moor.</p>
        
        <p>India (Mughal): Sentuhan India tampak pada menara masjid yang ramping dan menjulang, serta beberapa detail ornamen yang khas.</p>

        <p>Spanyol: Pengaruh Spanyol kuno dapat dirasakan melalui penggunaan jendela kaca patri yang indah dan berwarna-warni, yang membiaskan cahaya dengan cantik ke dalam ruang salat utama.</p>

        <p>Interior masjid tak kalah menakjubkan. Ruang salat utama berbentuk segi delapan yang unik ditopang oleh pilar-pilar marmer kokoh yang didatangkan langsung dari Italia. Sebuah lampu gantung kristal mewah dari Prancis menjadi pusat perhatian, menambah kesan agung pada ruangan. Dinding-dindingnya dihiasi kaligrafi indah dan ornamen bunga serta geometris yang menambah kekhusyukan suasana.</p>
        
       
