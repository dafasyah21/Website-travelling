        <h1 id="destination-title">GEDUNG LONSUM</h1>
        <p>Sebuah bangunan yang seolah membekukan waktu. Dengan fasad putih yang megah dan arsitektur Eropa klasik, Gedung London Sumatra, atau yang lebih akrab disapa Gedung Lonsum, bukan sekadar gedung perkantoran tua. Ia adalah monumen, saksi bisu perjalanan panjang Medan dari kota perkebunan hingga menjadi kota metropolitan, dan kini menjadi salah satu spot fotografi paling ikonik di Sumatera Utara.</p>

        <p>Bagi siapa pun yang melintasi kawasan Lapangan Merdeka dan Titik Nol Kota Medan, mustahil untuk tidak terpana oleh pesonanya. Gedung ini memancarkan aura sejarah yang kuat, mengundang setiap pasang mata untuk mengagumi detailnya dan membayangkan kisah-kisah yang tersimpan di balik dindingnya selama lebih dari satu abad.</p>

        <img src="image/gedunglondon.png">
        <h2>Pesona Arsitektur dan Sejarah yang Tak Lekang Waktu</h2>
        
        <p>Dibangun pada tahun 1906 oleh perusahaan perkebunan asal Inggris, Harrisons & Crosfield Company, Gedung Lonsum adalah cerminan kemakmuran Medan pada awal abad ke-20. Nama "London Sumatra" sendiri merujuk pada nama perusahaan yang kemudian dikenal sebagai PT PP London Sumatra Indonesia Tbk.</p>

        <p>Keistimewaan gedung ini tidak hanya terletak pada usianya, tetapi juga pada arsitekturnya yang mengadopsi gaya rumah-rumah di London pada era transisi. Dengan lima lantai, jendela-jendela besar yang simetris, dan pilar-pilar kokoh, bangunan ini tampak kontras namun serasi dengan lingkungan sekitarnya.</p>
       
        <p>Fakta menarik yang menjadikannya pelopor di zamannya adalah Gedung Lonsum merupakan gedung pertama di Kota Medan yang menggunakan teknologi lift (elevator). Fakta ini menegaskan statusnya sebagai simbol kemajuan dan modernitas pada masa itu. Kini, sebagai bangunan Cagar Budaya yang dilindungi, Gedung Lonsum terus dirawat dengan baik, mempertahankan keaslian dan kemegahannya.</p>

       
