        <h1 id="destination-title">Istana Maimun</h1>
        <p>Bagi para pelancong dan pencinta sejarah, Kota Medan di Sumatera Utara menawarkan sebuah permata yang tak lekang oleh waktu: Istana Maimun. Lebih dari sekadar bangunan bersejarah, istana ini merupakan saksi bisu kejayaan Kesultanan Deli dan sebuah kanvas megah yang memadukan beragam corak arsitektur dunia. Mari kita selami lebih dalam pesona Istana Maimun dan temukan informasi praktis yang Anda butuhkan untuk merencanakan kunjungan tak terlupakan.</p>

        <img src="image/bangunan.jpg">
        <h2>Sekilas Sejarah: Warisan Sultan untuk Sang Permaisuri</h2>

        <p>Istana Maimun didirikan atas perintah Sultan Deli ke-9, Sultan Ma'moen Al Rasyid Perkasa Alamsyah. Peletakan batu pertamanya dilakukan pada 26 Agustus 1888 dan selesai dibangun pada 18 Mei 1891. Nama "Maimun" sendiri diambil dari nama permaisuri sultan, Siti Maimunah, sebagai wujud cinta sang sultan.</p>

        <p>Istana seluas 2.772 meter persegi ini pernah menjadi pusat pemerintahan Kesultanan Deli dan kediaman resmi keluarga kerajaan. Kini, bangunan utama istana telah beralih fungsi menjadi museum yang memamerkan berbagai peninggalan bersejarah, sementara bagian sayap kiri dan kanan masih sesekali digunakan untuk acara adat.</p>

        <h2>Arsitektur Unik: Perpaduan Budaya yang Memukau</h2>

        <p>Salah satu daya tarik utama Istana Maimun adalah arsitekturnya yang eklektik. Dirancang oleh seorang arsitek Italia bernama Theodoor van Erp, istana ini secara harmonis memadukan unsur-unsur arsitektur Melayu, Islam, Spanyol, India, hingga Italia.

        Pengaruh Melayu kental terasa pada dominasi warna kuning keemasan yang melambangkan kebesaran dan atap berbentuk limas. Sentuhan Islam tecermin pada lengkungan-lengkungan di pintu dan jendela yang menyerupai kubah. Sementara itu, nuansa Eropa hadir melalui perabotan, jendela-jendela besar, dan pilar-pilar kokoh yang menghiasi bangunan. Perpaduan inilah yang menciptakan sebuah mahakarya arsitektur yang unik dan memanjakan mata.</p>

        <p>Di dalam istana, pengunjung akan disambut oleh singgasana sultan dan permaisuri yang megah, diiringi alunan musik tradisional Melayu yang dimainkan oleh pemusik lokal. Jangan lewatkan pula untuk mengagumi berbagai koleksi foto keluarga Kesultanan Deli, perabotan antik, dan artefak bersejarah lainnya.Tak jauh dari bangunan utama istana, terdapat sebuah bangunan kecil yang menyimpan salah satu ikon paling misterius dari Istana Maimun: Meriam Puntung. Menurut legenda yang beredar, meriam ini merupakan jelmaan dari Putri Hijau dari Kerajaan Aru yang berubah wujud untuk mempertahankan diri dari serangan Kerajaan Aceh. Konon, karena panasnya tembakan, meriam ini terbelah menjadi dua. Satu bagian berada di Istana Maimun, dan bagian lainnya terlempar hingga ke Desa Sukanalu di Dataran Tinggi Karo.</p>


